//Comparators and Equality

/*
=== -> is equal to
!== -> is not equal to 
>   -> is greater than
<   -> is lesser than
>=  -> is greater or equal to
<=  -> is lesser or  equal to 
*/

let n1 = 19;
let n2 = 2;

console.log(n1 === n2);
console.log(n1 !== n2);
console.log(n1 > n2);
console.log(n1 < n2);
console.log(n1 >= n2);
console.log(n1 <= n2);